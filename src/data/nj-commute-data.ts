/**
 * South Jersey cities where Philly commute should be shown prominently
 * (instead of or in addition to NYC)
 */
export const southJerseyCities = new Set([
  "Cherry Hill", "Camden", "Mount Laurel", "Moorestown", "Burlington",
  "Willingboro", "Evesham", "Voorhees", "Haddonfield", "Collingswood",
  "Gloucester City", "Deptford", "Washington Township", "Vineland",
  "Trenton", "Hamilton", "Ewing", "Phillipsburg",
]);

/**
 * Look up commute data for a city.
 * Tries exact match first, then case-insensitive.
 */
export function getCommuteData(city: string | null | undefined): CommuteData | null {
  if (!city) return null;
  const trimmed = city.trim();

  // Exact match
  if (commuteByCity[trimmed]) return commuteByCity[trimmed];

  // Case-insensitive match
  const lower = trimmed.toLowerCase();
  for (const [key, value] of Object.entries(commuteByCity)) {
    if (key.toLowerCase() === lower) return value;
  }

  return null;
}

/**
 * Check if a city is in South Jersey (show Philly commute)
 */
export function isSouthJersey(city: string | null | undefined): boolean {
  if (!city) return false;
  const trimmed = city.trim();
  if (southJerseyCities.has(trimmed)) return true;
  const lower = trimmed.toLowerCase();
  for (const c of southJerseyCities) {
    if (c.toLowerCase() === lower) return true;
  }
  return false;
}
